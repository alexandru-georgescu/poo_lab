package com.company;

public class Calculator {

    public int add(int nr1, int nr2) {
        return nr1 + nr2;
    }

    public int sub(int nr1, int nr2) {
        return nr1 - nr2;
    }

    public int mul(int nr1, int nr2) {
        return nr1 * nr2;
    }

    public int pow(int nr1) {
        return nr1 * nr1;
    }
}
